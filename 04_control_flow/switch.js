/* syntax: 
switch (key){
    case value:

        break;

    default:
        break;
}
*/
const month = 3
switch(month){
    case 1:
        console.log("january");
        break;    //shift+alt+downkey (to copy lines of code)
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;
    default:
        console.log("default case match");
        break;

}

